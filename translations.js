// Language management
let currentLanguage = localStorage.getItem('language') || 'en';

function setLanguage(lang) {
  currentLanguage = lang;
  localStorage.setItem('language', lang);
  document.documentElement.lang = lang;
  updatePageContent();
  updateLanguageSwitcher();
}

function getTranslation(key) {
  const keys = key.split('.');
  let value = translations[currentLanguage];
  for (const k of keys) {
    value = value?.[k];
  }
  return value || key;
}

function updateLanguageSwitcher() {
  const switchers = document.querySelectorAll('.lang-switcher');
  switchers.forEach(switcher => {
    const buttons = switcher.querySelectorAll('button');
    buttons.forEach(btn => {
      if (btn.dataset.lang === currentLanguage) {
        btn.classList.add('active');
      } else {
        btn.classList.remove('active');
      }
    });
  });
}

function updatePageContent() {
  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    const translation = getTranslation(key);
    if (element.tagName === 'INPUT' && element.type !== 'submit' && element.type !== 'button') {
      element.placeholder = translation;
    } else if (element.tagName === 'BUTTON' || element.tagName === 'INPUT' && (element.type === 'submit' || element.type === 'button')) {
      element.textContent = translation;
    } else {
      element.textContent = translation;
    }
  });
  
  // Update page title
  const pageTitle = document.querySelector('[data-i18n-title]');
  if (pageTitle) {
    document.title = getTranslation(pageTitle.getAttribute('data-i18n-title'));
  }
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', () => {
  document.documentElement.lang = currentLanguage;
  updatePageContent();
  updateLanguageSwitcher();
});

